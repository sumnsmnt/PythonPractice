# str[starting index : ending index]

# it will print 0 index to the last index
str = "Learn Python with Suman"
print(str[0:len(str)])

# it will also print 0 index to the last index
str = "Learn Python with Suman"
print(str[0:])

# it will print 6th index to 11th index
str = "Learn Python with Suman"
print(str[6:12])

# it will print 18th index to last index
str = "Learn Python with Suman"
print(str[18:])

# Negative Index Slicing
str = "Python"
print(str[-6:], str[-5:-2])