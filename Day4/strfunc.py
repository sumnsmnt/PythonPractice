str = "learn Python With Suman"

# returns TRUE if string ends with sub string
print(str.endswith("an"))

# Capitalize the first string
print(str.capitalize())

# Replace the word/letter with new one
print(str.replace("learn","Earn"))
print(str.replace("n","N"))

# returns 1st character/word index
print(str.find("S"))

# counts the number of occurrence
print(str.count("n"))